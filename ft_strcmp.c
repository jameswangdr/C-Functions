int ft_strcmp(char *s1, char *s2)
{
	int i;
	int result;
	
	while (s1[i] != '\0' && s2[i] != '\0')
		if (s1[i] == s2[i])
			i++;
		result = (s1[i] - s2[i]);
	return (result);
}
