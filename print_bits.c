void print_bits(unsigned char octet)
{
	int i;

	i = 128;
	while (i > 0)
	{
		if (octet >= i)
		{
			ft_putchar('1');
			octet = octet - i;
			i = i / 2;
		}
		else
		{
			ft_putchar('0');
			i = i / 2;
		}
	}
}
