#include <unistd.h>

int main ()
{
	char c;
	int i;


	i = 1;
	c = 'a';
	while (c != 'Z')
	{
		if (i %2 == 0)
		{
			c = c + 32;
			write(1, &c, 1);
			c = c - 32;
		}
		else
		{
			write(1, &c, 1);	
			i++;
			c++;	
		}
	}	
	return (0);
}
