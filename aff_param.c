#include <unistd.h>

int main (int argc, char **argv)
{
	int n;

	n = argc - 1;
	if (argc > 1)
		while (*argv[n])
		{	
			write (1, argv[n], 1);
			argv[n]++;
		}
	write (1, "\n", 1);
	return (0);
}
